package Operadores; // 9. Imprime 3 ejemplos de uso de operadores unarios.

public class p9 {
    public static void main(String[] args) {
        int x = 5;
        System.out.println(++x); //5+1
        System.out.println(--x); //(5+1)-1
        System.out.println(x--); //[(5+1)-1]-1
        System.out.println(x); //[(5+1)-1]-1
    }   
}
