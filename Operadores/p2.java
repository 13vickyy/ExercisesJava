package Operadores; // 2. Crea una variable para cada tipo de operación de asignación.

public class p2 {
    public static void main(String[] args) {
        
        int nro_1 = 10;

        nro_1 += 1;
        System.out.println(nro_1);
        nro_1 -= 1;
        System.out.println(nro_1);
        nro_1 *= 2;
        System.out.println(nro_1);
        nro_1 /= 1;
        System.out.println(nro_1);

    }
    
}
