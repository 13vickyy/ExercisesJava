package TipoDatos;

public class Primitivos {
    public static void main(String[] args) {
        
        int MyInt = 29;
        System.out.println(MyInt);

        double MyDouble = 3.14; //Estandar de "float" en JAVA
        System.out.println(MyDouble);

        char MyChar = 'A'; //Comillas SIMPLES. Dobles es para string
        System.out.println(MyChar);
        
        boolean MyBoolean = false;
        System.out.println(MyBoolean);

        String MyString = "Esto no es un tipo de dato primitivo, en realidad es una clase, pero se utiliza mucho";
        System.out.println(MyString);

    }
    
}
